    private void ordersActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ordersActionPerformed
        Receipt form3 = new Receipt();
        String TransactionMode = "";
        String TransactionType1 = (String) ComboOne.getSelectedItem();
        if (TransactionType1.equals("Dine In")) {
            TransactionMode = TransactionType1;
        } else if (TransactionType1.equals("Take Out")) {
            TransactionMode = TransactionType1;
        }
        if (ComboOne.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(null, "Please Select Transaction Type", "Message", JOptionPane.INFORMATION_MESSAGE);
        } else {
            double change = 0;

            String m = JOptionPane.showInputDialog(totalamt.getText() + "\n" + "Enter Amount: ");

            change = Double.parseDouble(m) - totalAmount;
            if (totalAmount < Double.parseDouble(m)) {

                DefaultTableModel model = (DefaultTableModel) myTable.getModel();
                form3.jLabel2.setText(jLabel154.getText());
                form3.TransactionType.setText("Transaction Type: " + TransactionMode);
                form3.jLabel5.setText("Amount Paid: ₱" + m + ".00");
                form3.jLabel6.setText(totalamt.getText());
                form3.jTextField112.setText(" ₱" + change);
                form3.jTable3.setModel(model);
                form3.setVisible(true);
                form3.SuperCard.setText("Customer Card Number: " + SuperCardNum);
                form3.jLabel2.setText("Customer Name: " + FrontForm.CustomerName);
            } else {
                JOptionPane.showMessageDialog(null, "Amount is not enough", "Warning", JOptionPane.WARNING_MESSAGE);
            }
        }

        int[] stlnum = new int[100000];
        Random rand = new Random();
        for (int a = 0; a < 1; a++) {
            int randomNum = rand.nextInt(stlnum.length);
            if (randomNum == 0) {
                randomNum = 111292391;
            }
            stlnum[a] = randomNum;
        }
        form3.TransactionNumber.setText("Transaction Number: 1192" + String.valueOf(stlnum[0]));
    }//GEN-LAST:event_ordersActionPerformed

    private void jButton40ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton40ActionPerformed
        model = (DefaultTableModel) myTable.getModel();
        if (myTable.getSelectedRowCount() == 1) {
            model.removeRow(myTable.getSelectedRow());
            totalAmount = 0;

            for (int idx = 0; idx < myTable.getRowCount(); idx++) {
                totalAmount = totalAmount + Double.parseDouble(myTable.getValueAt(idx, 3).toString());

            }
            tAmount2 = String.format("%.02f", totalAmount);
            totalamt.setText("Order Total: ₱" + totalAmount);
        } else {
            if (myTable.getSelectedRowCount() < 0) {
                JOptionPane.showMessageDialog(this, "Cart is Empty");
            } else {
                JOptionPane.showMessageDialog(this, "Please Select Single Row for Delete");
            }
        }

    }//GEN-LAST:event_jButton40ActionPerformed

