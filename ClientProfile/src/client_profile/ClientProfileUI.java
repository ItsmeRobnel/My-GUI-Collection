    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = "", age = "", gender = "", activities = "", athleticLevel = "", residence = "";
//for name
        name = jTextField1.getText();
//for age
        age = jTextField2.getText();
//for gender
        if (jRadioButton1.isSelected()) {
            gender = jRadioButton1.getText();
        }
        if (jRadioButton2.isSelected()) {
            gender = jRadioButton2.getText();
        }
//Activities
        if (jCheckBox1.isSelected()) {
            activities = activities + " " + jCheckBox1.getText() + '\n';
        }
        if (jCheckBox2.isSelected()) {
            activities = activities + " " + jCheckBox2.getText() + '\n';
        }
        if (jCheckBox3.isSelected()) {
            activities = activities + " " + jCheckBox3.getText() + '\n';
        }
        if (jCheckBox4.isSelected()) {
            activities = activities + " " + jCheckBox4.getText() + '\n';
        }
        if (jCheckBox5.isSelected()) {
            activities = activities + " " + jCheckBox5.getText() + '\n';
        }
//Athletic Level
        if (jRadioButton3.isSelected()) {
            athleticLevel = jRadioButton3.getText();
        }
        if (jRadioButton4.isSelected()) {
            athleticLevel = jRadioButton4.getText();
        }
        if (jRadioButton5.isSelected()) {
            athleticLevel = jRadioButton5.getText();
        }
        if (jRadioButton6.isSelected()) {
            athleticLevel = jRadioButton6.getText();
        }
//Residence
        residence = (String) comboOne.getSelectedItem();
//Exception Handler
        if (gender.equals("Male")) {
            if (name.equals("") || gender.equals("") || activities.equals("") || athleticLevel.equals("") || age.equals("") || residence.equals("Select Here")) {
                JOptionPane.showMessageDialog(this, "Incomplete Information", "Message", JOptionPane.INFORMATION_MESSAGE);
            } else {
                //Displaying male(Pane)
                javax.swing.JOptionPane.showMessageDialog(this, name + " is " + age + " years Old " + "\n" + "He is a " + gender + "\n" + "He lives in " + residence + "\n" + "He is " + athleticLevel + " Level Athlete" + "\n" + "\n" + "Activities include:" + "\n" + activities);
            }
        } else {
 if (name.equals("") || gender.equals("") || activities.equals("") || athleticLevel.equals("") || age.equals("")||residence.equals("Select Here")) {
                JOptionPane.showMessageDialog(this, "Incomplete Information", "Message", JOptionPane.INFORMATION_MESSAGE);
            } else {
                //Displaying female(Pane)
                javax.swing.JOptionPane.showMessageDialog(this, name + " is " + age + " years Old " + "\n" + "She is a " + gender + "\n" + "She lives in " + residence + "\n" + "She is " + athleticLevel + " Level Athlete" + "\n" + "\n" + "Activities include:" + "\n" + activities);
            }
        }

    }//GEN-LAST:event_jButton1ActionPerformed

