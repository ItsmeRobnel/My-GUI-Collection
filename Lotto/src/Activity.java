void generate_num(){
      int[] stlnum = new int[stltype];
      Random rand = new Random();
      for (int i = 1; i < stlnum.length; i++) {
          stlnum[i] = i;
      }
      
      for (int a = 1; a < stlnum.length; a++) {
          int randomNum = rand.nextInt(stlnum.length);
          if (randomNum == 0){
              randomNum++;
          }
          stlnum[a] = randomNum;
      }
      jTextField1.setText(Integer.toString(stlnum[1]));
      jTextField2.setText(Integer.toString(stlnum[2]));
      loopnum++;
      if (loopnum == 50){
          timer.stop();
      }
  }
void timeraction(){
      timer = new Timer(10, new ActionListener() {
          @Override
          public void actionPerformed(ActionEvent e) {
              generate_num();
          }
      });
  }
